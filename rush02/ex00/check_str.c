#include "numbers.h"

int		check_str(char *str, int *i)
{
	long	num;

	num = 0;
	while (str[*i] != '\n')
	{
		if (str[*i] >= 32 && str[*i] <= 126)
			num = 1;
		else
			break ;
		*i += 1;
	}
	*i += 1;
	return (num);
}
